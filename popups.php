<script>
    document.addEventListener('DOMContentLoaded', function () {
        // Находим все ссылки, которые открывают попапы
        const popupLinks = document.querySelectorAll('a[href^="#popup"]');

        popupLinks.forEach(function (link) {
            link.addEventListener('click', function (event) {
                event.preventDefault(); // Отменяем стандартное поведение ссылки

                // Получаем ID попапа из ссылки
                const popupId = this.getAttribute('href');
                const popup = document.querySelector(popupId);
                console.log(popupId)
                // Показываем попап
                if (popup) {
                    popup.style.display = 'flex';
                }
            });
        });

        // Закрытие попапа по крестику
        const closePopupButtons = document.querySelectorAll('.close-popup');

        closePopupButtons.forEach(function (button) {
            button.addEventListener('click', function () {
                const popup = this.closest('.overlay');
                popup.style.display = 'none';
            });
        });

        // Закрытие попапа при клике за его пределами
        const overlays = document.querySelectorAll('.overlay');

        overlays.forEach(function (overlay) {
            overlay.addEventListener('click', function (event) {
                if (event.target === overlay) {
                    overlay.style.display = 'none';
                }
            });
        });
    });
</script>
